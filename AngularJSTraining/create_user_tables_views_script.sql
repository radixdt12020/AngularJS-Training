CREATE VIEW [dbo].[vProducts]
AS
SELECT		PR.Id AS ProductId,
			PR.ProductName, 
			PR.Price,
			CA.Id As CategoryId,
			CA.CategoryName,
			BR.Id AS BrandId,
			BR.BrandName, 
			PR.Color, 
			PR.IsInStock, 
			PR.CreatedBy, 
			U.FullName,
			PR.CreatedDate,
			PR.LastModifiedBy,
			PR.LastModifiedDate
FROM		dbo.Products PR INNER JOIN
			dbo.Categories CA ON PR.CategoryId = CA.Id INNER JOIN
			dbo.Brands BR ON PR.BrandId = BR.Id INNER JOIN
			dbo.vUsers U ON PR.CreatedBy = U.Id
GO
